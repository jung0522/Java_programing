import java.lang.System; 												

public class Heart  															
{
	// 프로그램시작 
	public static void main( String[] args )  								
	{
		// 하트 모양을 출력 
		System.out.println( "\033[0;31m .d88b.d88b. \033[0m" ); 	
		System.out.println( "\033[0;33m 88888888888 \033[0m" );  
		System.out.println( "\033[0;32m `Y8888888Y' \033[0m" );  
		System.out.println( "\033[0;36m   `Y888Y' \033[0m" ); 	
		System.out.println( "\033[0;34m     `Y' \033[0m" );  	

		// 프로그램 종료 
		return; 		
	}
}
